#Tuple lar listelerle benzer yapıdadır .
#Listelerden farkı listelerde elemanları değiştirebiliyorken tuple da değiştirmek söz konusu değildir.
#Tuple veri yapısı performanslı data sağlar.

#empty tuple 
my_tuple = ()
print(my_tuple)

#tuple having integers 
my_tuple = (1,2,3,4)
print(my_tuple)

#tuple with mixed datatypes
my_tuple = (1,'Hello',1.4)
print(my_tuple)

#nested tuple 
my_tuple = ('mouse',[8,4,6],[1,2,3])
