# 22/12/2020 for iteration range

# 0 dan başlayıp 100 kez döner 99 da biter.
for x in range(100):
    print(x)
    
#Başlangıç ve bitiş değeri verilebilir. 
for x in range(1,100):
    print(x)
    
# Başlangıç bitiş ve artış değeri verilebilir.
for x in range(1,100,2):
    print(x)
    
    
for x in range(2,100,2):
    print(x)
