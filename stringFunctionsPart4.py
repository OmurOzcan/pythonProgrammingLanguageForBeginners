#12/12/2020 replace() fonksiyonu

#replace() fonksiyonu string içerisindeki karakterleri veya karakter dizilerini yeniden adlandırmamızı sağlar.

message = 'Hello World!'

print(message.replace('o','ö'))

message = message.replace('e','t') #message değişkeninin değerinin değişmesi için bu şekilde atama yapmalıyız.

print(message)
